}

to_crlf() {
  local file="$1"
  local tmp="$file.tmp"
  awk '{ sub(/\r$/, ""); printf "%s\r\n", $0 }' "$file" > "$tmp"
  mv "$tmp" "$file"
}

prepare_windows_text_files() {
  local bundle="$1"
  to_crlf "$bundle/启动系统.bat"
  to_crlf "$bundle/停止系统.bat"
  to_crlf "$bundle/使用说明.txt"
}

clean_runtime_state() {
  local bundle="$1"
  find "$bundle/data" "$bundle/logs" -mindepth 1 -exec rm -rf {} +
}

create_archive() {
  local platform="$1"
  local archive="$DIST_DIR/$APP_NAME-$platform.zip"

  rm -f "$archive"
  if [[ "$platform" == windows-* ]]; then
    # JDK jar writes UTF-8 ZIP filenames and sets the language encoding flag,
    # which prevents Chinese launcher filenames from becoming mojibake on Windows.
    jar --create --file "$archive" --no-manifest -C "$DIST_DIR" "$APP_NAME-$platform"
  else
    (cd "$DIST_DIR" && zip -qr "$APP_NAME-$platform.zip" "$APP_NAME-$platform")
  fi
}

create_bundle() {
  local platform="$1"
  local bundle="$DIST_DIR/$APP_NAME-$platform"
  local runtime_cache="$CACHE_DIR/jre/$platform"

  echo "==> Creating bundle: $platform"
  rm -rf "$bundle"
  mkdir -p "$bundle/app" "$bundle/runtime" "$bundle/data" "$bundle/logs"

  cp "$JAR_SOURCE" "$bundle/app/backend.jar"
  cp -R "$runtime_cache"/. "$bundle/runtime"/
  if [[ -f "$MYSQL_SNAPSHOT" ]]; then
    cp "$MYSQL_SNAPSHOT" "$bundle/app/mysql-snapshot.sql"
  fi

  if [[ "$platform" == windows-* ]]; then
    write_windows_launchers "$bundle"
  else
    write_macos_launchers "$bundle"
  fi

  write_bundle_readme "$bundle"
  if [[ "$platform" == windows-* ]]; then
    prepare_windows_text_files "$bundle"
  fi

  clean_runtime_state "$bundle"
  create_archive "$platform"
}

main() {
  if [[ "$SKIP_BUILD" -eq 0 ]]; then
    build_application
  fi

  generate_mysql_snapshot

  if [[ ! -f "$JAR_SOURCE" ]]; then
    echo "Backend jar not found: $JAR_SOURCE" >&2
    exit 1
  fi

  mkdir -p "$DIST_DIR"

  local platforms
  platforms="$(resolve_platforms)"
  for platform in $platforms; do
    download_runtime "$platform"
    create_bundle "$platform"
  done

  echo "==> Done. Packages are under $DIST_DIR"
}

main
